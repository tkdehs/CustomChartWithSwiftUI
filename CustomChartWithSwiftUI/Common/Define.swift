import SwiftUI

public func FONT_NOTOSANS_KR_REGULAR ( _ size : CGFloat ) -> Font   { return Font.custom("NotoSansKR-Regular", size: size) }
public func FONT_NOTOSANS_KR_MEDIUM ( _ size : CGFloat ) -> Font    { return Font.custom("NotoSansKR-Medium", size: size) }
public func FONT_NOTOSANS_KR_LIGHT ( _ size : CGFloat ) -> Font      { return Font.custom("NotoSansKR-Light", size: size) }
public func FONT_NOTOSANS_KR_BOLD ( _ size : CGFloat ) -> Font      { return Font.custom("NotoSansKR-Bold", size: size) }
